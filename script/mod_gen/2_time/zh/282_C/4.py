def main():
    # 输入
    N, M = map(int, input().split())
    S = [input() for _ in range(N)]
    # 预处理
    # S[i][j] == 'o' ならば 1, 'x' ならば 0 に置き換える
    for i in range(N):
        S[i] = [1 if c == 'o' else 0 for c in S[i]]
    # dp[i][j] := 集合 i に含まれる参加者のうち、問題 j を解くことができる参加者が存在するとき 1, 存在しないとき 0
    dp = [[0] * M for _ in range(1 << N)]
    for i in range(N):
        dp[1 << i] = S[i]
