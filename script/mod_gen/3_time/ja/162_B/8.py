def main():
    N = int(input())
    # N = 15
    # N = 1000000
    # 3の倍数の和
    n_3 = N // 3
    sum_3 = (n_3 * (n_3 + 1)) // 2 * 3
    # 5の倍数の和
    n_5 = N // 5
    sum_5 = (n_5 * (n_5 + 1)) // 2 * 5
    # 15の倍数の和
    n_15 = N // 15
    sum_15 = (n_15 * (n_15 + 1)) // 2 * 15
    # 3の倍数と5の倍数の和
    print(sum_3 + sum_5 - sum_15)

if __name__ == '__main__':
    main()