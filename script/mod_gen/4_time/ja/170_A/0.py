def main():
    # input
    x1, x2, x3, x4, x5 = map(int, input().split())
    # compute
    # output
    if x1 == 0:
        print(1)
    elif x2 == 0:
        print(2)
    elif x3 == 0:
        print(3)
    elif x4 == 0:
        print(4)
    else:
        print(5)

if __name__ == '__main__':
    main()