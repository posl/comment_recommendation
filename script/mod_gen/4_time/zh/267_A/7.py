def isWeekend(s):
    if s == '星期一':
        return 1
    elif s == '星期二':
        return 2
    elif s == '星期三':
        return 3
    elif s == '星期四':
        return 4
    elif s == '星期五':
        return 5
    elif s == '星期六':
        return 6
    elif s == '星期日':
        return 7

if __name__ == '__main__':
    isWeekend()