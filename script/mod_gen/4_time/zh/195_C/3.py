def f(n):
    if n < 1000:
        return 0
    if n < 1000000:
        return n // 1000
    if n < 1000000000:
        return n // 1000000 + 999
    if n < 1000000000000:
        return n // 1000000000 + 1998
    if n < 1000000000000000:
        return n // 1000000000000 + 2997
    if n < 1000000000000000000:
        return n // 1000000000000000 + 3996
    return n // 1000000000000000000 + 4995

if __name__ == '__main__':
    f()