def solve():
    day = input()
    if day == '星期日':
        print(0)
    elif day == '星期一':
        print(5)
    elif day == '星期二':
        print(4)
    elif day == '星期三':
        print(3)
    elif day == '星期四':
        print(2)
    elif day == '星期五':
        print(1)
    elif day == '星期六':
        print(0)

if __name__ == '__main__':
    solve()