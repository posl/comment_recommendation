def change(N):
    # 确定用了多少张1000日元的纸币
    num_1000 = N // 1000
    # 确定用了多少张100日元的纸币
    num_100 = N % 1000 // 100
    # 确定用了多少张10日元的纸币
    num_10 = N % 1000 % 100 // 10
    # 确定用了多少张1日元的纸币
    num_1 = N % 1000 % 100 % 10 // 1
    return num_1000 + num_100 + num_10 + num_1

if __name__ == '__main__':
    change()