def main():
    n = int(input())
    if n < 111:
        print(111)
    elif 111 <= n < 222:
        print(111)
    elif 222 <= n < 333:
        print(222)
    elif 333 <= n < 444:
        print(333)
    elif 444 <= n < 555:
        print(444)
    elif 555 <= n < 666:
        print(555)
    elif 666 <= n < 777:
        print(666)
    elif 777 <= n < 888:
        print(777)
    elif 888 <= n < 999:
        print(888)
    else:
        print(999)

if __name__ == '__main__':
    main()