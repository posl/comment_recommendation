def answer(A, B):
    if A == 1 and B == 2:
        return 3
    elif A == 2 and B == 3:
        return 1
    elif A == 3 and B == 1:
        return 2
    elif A == 1 and B == 3:
        return 2
    elif A == 2 and B == 1:
        return 3
    elif A == 3 and B == 2:
        return 1

if __name__ == '__main__':
    answer()