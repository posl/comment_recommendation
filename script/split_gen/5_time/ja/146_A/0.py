def main():
    s = input()
    ans = 0
    if s == "SUN":
        ans = 7
    elif s == "MON":
        ans = 6
    elif s == "TUE":
        ans = 5
    elif s == "WED":
        ans = 4
    elif s == "THU":
        ans = 3
    elif s == "FRI":
        ans = 2
    elif s == "SAT":
        ans = 1
    print(ans)
