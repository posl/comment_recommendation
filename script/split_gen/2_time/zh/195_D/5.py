def main():
    N = int(input())
    if N < 1000:
        print(0)
    elif N < 1000000:
        print(N-999)
    elif N < 1000000000:
        print(2*(N-999999)+999000)
    elif N < 1000000000000:
        print(3*(N-999999999)+1998000000)
    elif N < 1000000000000000:
        print(4*(N-999999999999)+2999700000000)
    else:
        print(5*(N-999999999999999)+3999960000000000)
