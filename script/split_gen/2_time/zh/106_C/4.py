def replace_num(num):
    num = str(num)
    num = num.replace('2','22')
    num = num.replace('3','333')
    num = num.replace('4','4444')
    num = num.replace('5','55555')
    num = num.replace('6','666666')
    num = num.replace('7','7777777')
    num = num.replace('8','88888888')
    num = num.replace('9','999999999')
    return num
