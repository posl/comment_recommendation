def main():
    x = int(input())
    y = int(input())
    if x == 1:
        if y == 2:
            print(3)
        else:
            print(2)
    elif x == 2:
        if y == 1:
            print(3)
        else:
            print(1)
    else:
        if y == 1:
            print(2)
        else:
            print(1)
