def process_str(str):
    str = str.replace('2','22')
    str = str.replace('3','333')
    str = str.replace('4','4444')
    str = str.replace('5','55555')
    str = str.replace('6','666666')
    str = str.replace('7','7777777')
    str = str.replace('8','88888888')
    str = str.replace('9','999999999')
    return str
