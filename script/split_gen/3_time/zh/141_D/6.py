def solve(n, m, a):
    # 简单的贪心算法
    # 由于 2^30 > 10^9，所以最多只能使用30张门票
    # 从最低价的商品开始购买，使用尽可能多的门票
    # 如果使用了i张门票，那么购买该商品的价格为 a[i] / (2^i)
    # 由于 a[i] 是整数，所以 a[i] / (2^i) = a[i] >> i
    # 由于 a[i] 最大是10^9，所以 a[i] >> i 最大是 10^9 / (2^30) = 0
    # 所以最多只需要30张门票
    a.sort()
    ans = 0
    for i in range(n):
        ans += a[i] >> i
    return ans
