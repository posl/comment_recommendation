def calc(N, X, Y):
    if N == 1:
        return 0
    if N == 2:
        return X + Y
    if N == 3:
        return X * 2 + Y
    if N == 4:
        return X * 3 + Y
    if N == 5:
        return X * 4 + Y
    if N == 6:
        return X * 5 + Y
    if N == 7:
        return X * 6 + Y
    if N == 8:
        return X * 7 + Y
    if N == 9:
        return X * 8 + Y
    if N == 10:
        return X * 9 + Y
    return 0
