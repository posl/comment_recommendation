def solve(n):
    if n < 11:
        return 0
    if n < 111:
        return 9
    if n < 1111:
        return 9 + 9
    if n < 11111:
        return 9 + 9 + 90
    if n < 111111:
        return 9 + 9 + 90 + 900
    if n < 1111111:
        return 9 + 9 + 90 + 900 + 9000
    if n < 11111111:
        return 9 + 9 + 90 + 900 + 9000 + 90000
    if n < 111111111:
        return 9 + 9 + 90 + 900 + 9000 + 90000 + 900000
    if n < 1111111111:
        return 9 + 9 + 90 + 900 + 9000 + 90000 + 900000 + 9000000
    if n < 11111111111:
        return 9 + 9 + 90 + 900 + 9000 + 90000 + 900000 + 9000000 + 90000000
