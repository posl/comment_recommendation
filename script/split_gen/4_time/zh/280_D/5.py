def find_min_factorial(k):
    if k == 2:
        return 2
    elif k == 3:
        return 3
    elif k == 4:
        return 4
    elif k == 5:
        return 5
    elif k == 6:
        return 3
    elif k == 7:
        return 7
    elif k == 8:
        return 8
    elif k == 9:
        return 6
    else:
        return 9
