def checkline(p1,p2,p3):
    if p1[0] == p2[0] and p2[0] == p3[0]:
        return True
    elif p1[1] == p2[1] and p2[1] == p3[1]:
        return True
    elif p1[0] == p2[0] and p2[0] == p3[0]:
        return True
    elif p1[0] == p2[0] and p2[0] == p3[0]:
        return True
    else:
        return False
